from __future__ import annotations

import re

import numpy as np
import sympy
from sympy import Symbol, symbols
from sympy.parsing.sympy_parser import (
    convert_xor,
    parse_expr,
    standard_transformations,
)

from algorithm_arena.optimizers.base import ObjectiveFn

_ALLOWED_FUNCTIONS = {
    "sin": sympy.sin,
    "cos": sympy.cos,
    "tan": sympy.tan,
    "exp": sympy.exp,
    "log": sympy.log,
    "sqrt": sympy.sqrt,
    "Abs": sympy.Abs,
    "asin": sympy.asin,
    "acos": sympy.acos,
    "atan": sympy.atan,
    "sinh": sympy.sinh,
    "cosh": sympy.cosh,
    "tanh": sympy.tanh,
    "Symbol": Symbol,  # the code generated by the auto_symbol transformation calls this
}

# We remove the auto_number transformation from standard_transformations: it
# rewrites numeric literals (e.g. "2") into Integer(2)/Float(2) calls, which
# clashed with global_dict={"__builtins__": {}} and caused a
# "name 'Integer' is not defined" error. With the transformation removed,
# numeric literals stay as plain Python int/float, which is already enough
# for expressions like x**2 and even reduces whitelist maintenance.
_TRANSFORMATIONS = tuple(
    t for t in standard_transformations if t.__name__ != "auto_number"
) + (convert_xor,)

_ALLOWED_CHARS_PATTERN = re.compile(r"^[0-9a-zA-Z\s+\-*/.(),^]+$")


class InvalidExpressionError(Exception):
    """Raised when the user-provided mathematical expression cannot be parsed
    or contains a disallowed construct."""


def parse_expression(expression_str: str) -> tuple[ObjectiveFn, sympy.Expr]:
    """
    Safely parses the string expression written by the user.

    Security layers:
      1. Character whitelist (regex) — filters out dunder/special characters
         up front.
      2. Restricting local_dict to only x, y, and the allowed math functions —
         every undefined name raises a NameError.
      3. Explicitly emptying __builtins__ in global_dict — prevents sympy's
         eval fallback from reaching the real Python builtins.
      4. Disabling the auto_number transformation — requires no extra
         whitelist maintenance for numeric literals and shrinks the attack
         surface.
    """
    if not _ALLOWED_CHARS_PATTERN.match(expression_str):
        raise InvalidExpressionError(
            f"Expression contains disallowed characters: '{expression_str}'. "
            f"Only numbers, x, y, +, -, *, /, ., (), , and math function "
            f"names (sin, cos, exp, sqrt, log, ...) are allowed."
        )

    x, y = symbols("x y", real=True)
    local_dict = {"x": x, "y": y, **_ALLOWED_FUNCTIONS}

    try:
        expr = parse_expr(
            expression_str,
            local_dict=local_dict,
            global_dict={"__builtins__": {}},
            transformations=_TRANSFORMATIONS,
            evaluate=True,
        )
    except Exception as e:
        raise InvalidExpressionError(
            f"Could not parse expression: '{expression_str}'. Error: {e}"
        ) from e

    # Because auto_number is disabled, purely numeric expressions like "5"
    # may now come back as bare Python int/float instead of sympy.Expr.
    # The downstream free_symbols and lambdify calls expect a sympy.Expr,
    # so we deliberately wrap it into a sympy type here.
    if isinstance(expr, (int, float)):
        expr = sympy.Integer(expr) if isinstance(expr, int) else sympy.Float(expr)

    if not isinstance(expr, sympy.Expr):
        raise InvalidExpressionError(
            f"'{expression_str}' is not a valid mathematical expression."
        )
    free_symbols = expr.free_symbols
    allowed_symbols = {x, y}
    unexpected = free_symbols - allowed_symbols
    if unexpected:
        raise InvalidExpressionError(
            f"Only the variables 'x' and 'y' are supported. "
            f"Unexpected symbol(s): {unexpected}"
        )

    numeric_fn = sympy.lambdify((x, y), expr, modules="numpy")

    def objective_fn(positions: np.ndarray) -> np.ndarray:
        x_vals = positions[:, 0]
        y_vals = positions[:, 1]
        result = numeric_fn(x_vals, y_vals)
        if np.isscalar(result):
            result = np.full(positions.shape[0], result, dtype=float)
        return np.asarray(result, dtype=float)

    return objective_fn, expr
